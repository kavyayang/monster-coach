// ── Main API ─────────────────────────────────────────────────────────

export function getCoachMessage(
  heartRate: number,
  score: number,
  action: string,
  isFormDeformed: boolean = false,
  personality: CoachPersonality = 'gentle',
): CoachResult {
  const zone = getHRZone(heartRate);

  const tier: ScoreTier =
    zone === 'danger'
      ? isFormDeformed
        ? 'poor'
        : 'perfect'
      : getScoreTier(score);

  const pool = COACH_MATRIX[personality][zone][tier];
  const raw = pick(pool);
  const message = raw.replace(/\{action\}/g, action);

  const isAlert = zone === 'danger' || tier === 'poor';

  return { message, isAlert };
}
